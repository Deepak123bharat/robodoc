    {opt="--css",help="Specify the stylesheet to use.",value={"file"}},
    {opt="--ext",help="Set extension for generated files.",value={"string"}},
    {opt="--footless",help="Do not create the foot of a document.",value={}},
    {opt="--headless",help="Do not create the head of a document.",value={}},
    {opt="--index",help="Add an index.",value={}},
    {opt="--internal",help="Also include internal headers.",value={}},
    {opt="--internalonly",help="Only include internal headers.",value={}},
    {opt="--lock",help="Recognize only one header marker per file.",value={}},
    {opt="--nodesc",help="Do not descent into subdirectories.",value={}},
    {opt="--no_subdirectories",help="Do no create documentation subdirectories.",value={}},
    {opt="--nopre",help="Do not use <PRE> </PRE> in the HTML output.",value={}},
    {opt="--nosort",help="Do not sort the headers.",value={}},
    {opt="--nosource",help="Do not include SOURCE items.",value={}},
