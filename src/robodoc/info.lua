usage = [[Usage:
	robodoc --src <directory> --doc <directory> --multidoc   [type] [options]
	robodoc --src <directory> --doc <file>      --singledoc  [type] [options]
	robodoc --src <file>      --doc <file>      --singlefile [type] [options]

    Type:
	--html, --rtf, --latex, --ascii, --dbxml, --troff]]
	
-- options table
-- opt contains the option trigger
-- help contains the help description for the option
-- value is a table containing validation information for the option value. 
-- 		** The length of the table is the number of values allowed.
--		** Table values can be "string", "number", "file", {table of allowed values},"list" (comma separated elements),"listN" (N elements comma separated)
